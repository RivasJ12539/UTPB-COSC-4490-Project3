package src.Audio;

public class AudioSettings {
    private double fxVolume = 0.0;
    private double musicVolume = 0.0;

    public double getFxVolume() {
        return fxVolume;
    }

    public void setFxVolume(double v) {
        fxVolume = v;
    }

    public double getMusicVolume() {
        return musicVolume;
    }

    public void setMusicVolume(double v) {
        musicVolume = v;
    }

}
